import math

#pi is a default value in math for greek pi
print(math.pi)

#e is a default value in math for Nepero's number
print(math.e)

#math.sqrt() is used to determine the square root of a number
print(math.sqrt(9))

#math.ceil() always rounds up a float
print(math.ceil(math.e))

#math.floor() always rounds down a float
print(math.floor(math.pi))